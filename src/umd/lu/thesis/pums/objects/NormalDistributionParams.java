package umd.lu.thesis.pums.objects;

/**
 *
 * @author lousia
 */
public class NormalDistributionParams {
    private double mean;
    private double sd;

    /**
     * @return the mean
     */
    public double getMean() {
        return mean;
    }

    /**
     * @param mean the mean to set
     */
    public void setMean(double mean) {
        this.mean = mean;
    }

    /**
     * @return the sd
     */
    public double getSd() {
        return sd;
    }

    /**
     * @param sd the sd to set
     */
    public void setSd(double sd) {
        this.sd = sd;
    }
    
}
